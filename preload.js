const { contextBridge, ipcRenderer } = require('electron')

contextBridge.exposeInMainWorld('gameAPI', {
  init:           ()           => ipcRenderer.invoke('game:init'),
  runEncounter:   ()           => ipcRenderer.invoke('game:runEncounter'),
  engageCombat:   ()           => ipcRenderer.invoke('game:engageCombat'),
  tryFlee:        ()           => ipcRenderer.invoke('game:tryFlee'),
  selectZone:     (zoneId)     => ipcRenderer.invoke('game:selectZone', zoneId),
  renderBag:      ()           => ipcRenderer.invoke('game:renderBag'),
  renderStats:    ()           => ipcRenderer.invoke('game:renderStats'),
  renderMap:      ()           => ipcRenderer.invoke('game:renderMap'),
  renderShop:     ()           => ipcRenderer.invoke('game:renderShop'),
  getShopData:    ()           => ipcRenderer.invoke('game:getShopData'),
  buyItem:        (id, qty, keeperName) => ipcRenderer.invoke('game:buyItem', id, qty, keeperName),
  sellItem:       (id, qty)    => ipcRenderer.invoke('game:sellItem', id, qty),
  renderParty:    ()           => ipcRenderer.invoke('game:renderParty'),
  getCraftingData: ()           => ipcRenderer.invoke('game:getCraftingData'),
  craftItem:       (recipeId)  => ipcRenderer.invoke('game:craftItem', recipeId),
  skinCorpses:    ()           => ipcRenderer.invoke('game:skinCorpses'),
  equipItem:      (id, iid)   => ipcRenderer.invoke('game:equipItem', id, iid),
  rezMember:      (instanceId) => ipcRenderer.invoke('game:rezMember', instanceId),
  allocateStat:   (instanceId, stat) => ipcRenderer.invoke('game:allocateStat', instanceId, stat),
  useItem:        (itemId)     => ipcRenderer.invoke('game:useItem', itemId),
  back:           ()           => ipcRenderer.invoke('game:back'),
  getItemCatalog:        ()        => ipcRenderer.invoke('game:getItemCatalog'),
  getBuffCatalog:        ()        => ipcRenderer.invoke('game:getBuffCatalog'),
  getQuestCatalog:       ()        => ipcRenderer.invoke('game:getQuestCatalog'),
  setCombatMode:         (mode)    => ipcRenderer.invoke('game:setCombatMode', mode),
  executePlayerAction:   (actions) => ipcRenderer.invoke('game:executePlayerAction', actions),
  getRosterData:         ()        => ipcRenderer.invoke('game:getRosterData'),
  swapPartyMember:       (outId, inId) => ipcRenderer.invoke('game:swapPartyMember', outId, inId),
  removeFromParty:       (id)          => ipcRenderer.invoke('game:removeFromParty', id),
  addToParty:            (id)          => ipcRenderer.invoke('game:addToParty', id),
  setPetForCompanion:    (id, petId)   => ipcRenderer.invoke('game:setPetForCompanion', id, petId),
  getAvailablePets:      (id)          => ipcRenderer.invoke('game:getAvailablePets', id),
  listSaveSlots:         ()        => ipcRenderer.invoke('game:listSaveSlots'),
  saveToSlot:            (slotId, saveName) => ipcRenderer.invoke('game:saveToSlot', slotId, saveName),
  loadFromSlot:          (slotId) => ipcRenderer.invoke('game:loadFromSlot', slotId),
  deleteSaveSlot:        (slotId) => ipcRenderer.invoke('game:deleteSaveSlot', slotId),
})
